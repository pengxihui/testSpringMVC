package com.wx.menu.domain;  

public class ClickButton {
	
     private String type;
     private String name;
     private String key;
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type 
	 * the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name 
	 * the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the key
	 */
	public String getKey() {
		return key;
	}
	/**
	 * @param key 
	 * the key to set
	 */
	public void setKey(String key) {
		this.key = key;
	}
}
  